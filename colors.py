def pr_red(prt): print("\033[91m {}\033[00m".format(prt))


def pr_green(prt): print("\033[92m {}\033[00m".format(prt))


def pr_yellow(prt): print("\033[93m {}\033[00m".format(prt))


def pr_blue(prt): print("\033[94m {}\033[00m".format(prt))


def pr_magenta(prt): print("\033[95m {}\033[00m".format(prt))


def pr_cyan(prt): print("\033[96m {}\033[00m".format(prt))


def pr_black(prt): print("\033[97m {}\033[00m".format(prt))
